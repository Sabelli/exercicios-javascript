let cor = "azul";
switch (cor) {
  case "vermelho":
    console.log("Parar!");
    break;
  case "amarelo":
    console.log("Atenção!");
    break;
  case "azul":
    console.log("Cor favorita selecionada!");
    break;
  default:
    console.log("Cor não reconhecida.");
}
// Saída: Cor favorita selecionada!